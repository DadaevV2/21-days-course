function generateInsights(reports, emotionScores, topThemes) {
    const insights = [];
    
    if (reports.length >= 14) {
        insights.push(`За ${reports.length} дней ты создал(а) целостную картину своего внутреннего мира`);
    }
    
    if (topThemes.length > 0) {
        insights.push(`Главные темы твоего пути: ${topThemes.join(', ')}`);
    }
    
    if (emotionScores.growth > emotionScores.negative) {
        insights.push('Ты чаще фокусируешься на росте, чем на трудностях — это признак зрелости');
    } else if (emotionScores.negative > 0) {
        insights.push('Ты честно встречаешься с трудностями — это основа настоящего роста');
    }
    
    if (emotionScores.positive > 5) {
        insights.push('Ты умеешь замечать хорошее — это основа устойчивого благополучия');
    }
    
    if (reports.length === 21) {
        insights.push('🏆 Ты прошёл полный цикл трансформации — это редкость и ценность');
    }
    
    return insights;
}

function generateForecast(trend, depth, days) {
    if (days < 10) {
        return `Ты в начале пути. При текущей глубине рефлексии через 21 день ты увидишь первые устойчивые изменения в мышлении.`;
    }
    if (trend === 'восходящий') {
        return `Твой рост ускоряется. Через 3 месяца такой практики ты заметишь качественные изменения в реакциях, решениях и отношениях.`;
    }
    if (trend === 'равномерный') {
        return `Ты идёшь стабильно. Это надёжный путь — через полгода ты будешь другим человеком.`;
    }
    return `Ты прошёл важную фазу. Сейчас стоит закрепить достигнутое и поставить новые вызовы.`;
}

