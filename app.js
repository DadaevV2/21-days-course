function markIntroComplete() { try { localStorage.setItem(INTRO_STATE_KEY, 'true'); } catch(e){} }
function isIntroComplete() { try { return localStorage.getItem(INTRO_STATE_KEY) === 'true'; } catch(e){ return false; } }

function getTodayIndex() {
    if (!state.user || !state.user.startDate) return 0;
    const start = new Date(state.user.startDate);
    const now = new Date();
    const diff = Math.floor((now - start) / (1000 * 60 * 60 * 24));
    return Math.min(Math.max(diff, 0), 20);
}

function updateDayFromCalendar() {
    if (!state.user) return;
    state.currentDay = getTodayIndex() + 1;
    if (state.currentDay > 21) state.currentDay = 21;
}

function escapeHtml(text) { const div = document.createElement('div'); div.textContent = text; return div.innerHTML; }

function getCourse() {
    if (!state.user || !state.user.course) return courses.strength;
    return courses[state.user.course] || courses.strength;
}

function getDayContent(dayNum) {
    const course = getCourse();
    const idx = Math.min(dayNum - 1, course.days.length - 1);
    return course.days[idx];
}

