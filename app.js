function loadState() {
    try {
        const saved = localStorage.getItem(APP_STATE_KEY);
        if (saved) return JSON.parse(saved);
    } catch (e) { console.error('Ошибка загрузки:', e); }
    return { user: null, reports: [], currentDay: 1, stageStatus: { morning: false, day: false, evening: false }, finished: false, analysis: null };
}

function saveState() {
    if (!state.user) return false;
    try {
        localStorage.setItem(APP_STATE_KEY, JSON.stringify({
            user: state.user, reports: state.reports, currentDay: state.currentDay,
            stageStatus: state.stageStatus, finished: state.finished, analysis: state.analysis
        }));
        return true;
    } catch (e) { console.error('Ошибка сохранения:', e); return false; }
}

