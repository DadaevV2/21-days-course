function generateAnalysis() {
    const reports = state.reports;
    if (reports.length === 0) return;

    const allText = reports.map(r => r.text).join(' ');
    const allTextLower = allText.toLowerCase();
    
    // 1. ЭМОЦИОНАЛЬНЫЙ АНАЛИЗ
    const emotions = {
        positive: ['радость', 'счастлив', 'благодар', 'горжусь', 'вдохновл', 'доволен', 'успех', 'победа', 'любовь', 'спокоен', 'уверен', 'энергия', 'сил', 'свет', 'тепло', 'уют'],
        negative: ['страх', 'тревог', 'устал', 'сложно', 'трудно', 'боль', 'злюсь', 'разочар', 'сомнев', 'виню', 'слаб', 'не могу', 'не знаю', 'грусть', 'одиноч'],
        growth: ['понял', 'осознал', 'научился', 'изменил', 'решил', 'выбрал', 'сделал', 'начал', 'преодолел', 'принял', 'отпустил', 'увидел', 'почувствовал']
    };
    
    const emotionScores = {};
    Object.keys(emotions).forEach(key => {
        emotionScores[key] = emotions[key].filter(word => allTextLower.includes(word)).length;
    });
    
    const dominantEmotion = Object.entries(emotionScores).sort((a, b) => b[1] - a[1])[0];
    
    // 2. ТЕМАТИЧЕСКИЙ АНАЛИЗ
    const themes = {
        'Отношения': ['друг', 'семья', 'любовь', 'общение', 'поддержка', 'помощь', 'разговор', 'близк'],
        'Работа/Цели': ['работ', 'цел', 'проек', 'карьер', 'бизнес', 'задач', 'план', 'результат'],
        'Здоровье': ['тело', 'сон', 'спорт', 'энерги', 'здоров', 'сил', 'устал', 'отдых'],
        'Саморазвитие': ['книг', 'учусь', 'знани', 'навык', 'опыт', 'рост', 'развит', 'практик'],
        'Внутренний мир': ['чувств', 'эмоци', 'мысл', 'осознан', 'медитац', 'внутр', 'душ', 'сознан'],
        'Преодоление': ['страх', 'трудност', 'преодол', 'барьер', 'вызов', 'испытан', 'кризис']
    };
    
    const themeScores = {};
    Object.entries(themes).forEach(([theme, words]) => {
        themeScores[theme] = words.filter(w => allTextLower.includes(w)).length;
    });
    
    const topThemes = Object.entries(themeScores)
        .filter(([, score]) => score > 0)
        .sort((a, b) => b[1] - a[1])
        .slice(0, 3)
        .map(([theme]) => theme);
    
    // 3. ПРОГРЕССИЯ МЫШЛЕНИЯ
    const earlyReports = reports.slice(0, 7).map(r => r.text.toLowerCase()).join(' ');
    const lateReports = reports.slice(-7).map(r => r.text.toLowerCase()).join(' ');
    
    const growthIndicators = ['понял', 'осознал', 'научился', 'изменил', 'принял', 'увидел'];
    const earlyGrowth = growthIndicators.filter(w => earlyReports.includes(w)).length;
    const lateGrowth = growthIndicators.filter(w => lateReports.includes(w)).length;
    
    const progressTrend = lateGrowth > earlyGrowth ? 'восходящий' : 
                          lateGrowth < earlyGrowth ? 'стабилизирующийся' : 'равномерный';
    
    // 4. КЛЮЧЕВЫЕ СЛОВА
    const words = allTextLower.match(/[а-яё]{4,}/g) || [];
    const stopWords = ['чтобы', 'сегодня', 'своему', 'своей', 'своих', 'свою', 'было', 'быть', 'меня', 'себя', 'тебя', 'этого', 'этом', 'этот', 'котор', 'поэто', 'потом', 'очень', 'просто', 'может', 'пожалуй', 'наверн'];
    const wordFreq = {};
    words.forEach(w => {
        if (!stopWords.includes(w) && w.length > 4) {
            wordFreq[w] = (wordFreq[w] || 0) + 1;
        }
    });
    const topWords = Object.entries(wordFreq)
        .filter(([, count]) => count >= 2)
        .sort((a, b) => b[1] - a[1])
        .slice(0, 8)
        .map(([word]) => word);
    
    // 5. ГЛУБИНА РЕФЛЕКСИИ
    const reflectionDepth = reports.reduce((sum, r) => {
        const len = r.text.length;
        const questions = (r.text.match(/\?/g) || []).length;
        const insights = growthIndicators.filter(w => r.text.toLowerCase().includes(w)).length;
        return sum + (len > 200 ? 3 : len > 100 ? 2 : 1) + questions + insights;
    }, 0);
    
    const depthLevel = reflectionDepth > 50 ? 'Глубокая рефлексия' :
                       reflectionDepth > 25 ? 'Осознанный анализ' : 'Поверхностное наблюдение';
    
    // 6. ФОРМИРОВАНИЕ ОТЧЁТА
    const course = getCourse();
    
    const emotionLabels = {
        positive: '🌟 Позитивный настрой',
        negative: '🌊 Работа с трудностями',
        growth: '🌱 Активный рост'
    };
    
    const recommendations = generateRecommendations({
        dominantEmotion: dominantEmotion[0],
        topThemes,
        progressTrend,
        depthLevel,
        courseType: state.user.course
    });
    
    state.analysis = {
        generated: new Date().toISOString(),
        course: `${course.icon} ${course.name}`,
        totalDays: reports.length,
        summary: {
            dominantEmotion: emotionLabels[dominantEmotion[0]] || '🎭 Смешанный фон',
            topThemes,
            progressTrend,
            depthLevel,
            keyWords: topWords
        },
        insights: generateInsights(reports, emotionScores, topThemes),
        recommendations,
        forecast: generateForecast(progressTrend, depthLevel, reports.length)
    };
    
    saveState();
}

