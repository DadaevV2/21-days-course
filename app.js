function renderDashboard() {
    if (!state.user) { showRegister(); return; }

    updateDayFromCalendar();
    saveState();

    const day = state.currentDay;
    const course = getCourse();
    const dayContent = getDayContent(day);

    displayName.textContent = state.user.name || 'Друг';
    currentDaySpan.textContent = day;
    courseBadge.textContent = `${course.icon} ${course.name}`;

    const progress = Math.min((state.reports.length / 21) * 100, 100);
    progressFill.style.width = progress + '%';
    progressText.textContent = Math.round(progress) + '%';

    statReports.textContent = state.reports.length;
    statChars.textContent = state.reports.reduce((sum, r) => sum + r.text.length, 0);
    const streak = Math.round((state.reports.length / Math.max(day, 1)) * 100);
    statStreak.textContent = streak + '%';

    const status = state.stageStatus;

    // УТРО
    if (status.morning && dayContent) {
        morningConcept.textContent = dayContent.morning.concept;
        morningScience.textContent = dayContent.morning.science;
        morningPractice.textContent = dayContent.morning.practice;
        morningQuote.textContent = '«' + dayContent.morning.quote + '»';
        morningDeep.style.display = 'block';
        morningPlaceholder.style.display = 'none';
    } else {
        morningDeep.style.display = 'none';
        morningPlaceholder.style.display = 'block';
    }

    // ДЕНЬ
    if (status.day && dayContent) {
        dayQuestion.textContent = dayContent.day.question;
        dayMicro.textContent = dayContent.day.micro;
        dayDeep.style.display = 'block';
        dayPlaceholder.style.display = 'none';
    } else {
        dayDeep.style.display = 'none';
        dayPlaceholder.style.display = 'block';
    }

    eveningQuestion.textContent = dayContent ? dayContent.day.question : "Какой урок ты усвоил сегодня?";

    morningStatus.textContent = status.morning ? '✅' : '⏳';
    dayStatus.textContent = status.day ? '✅' : '⏳';
    eveningStatus.textContent = status.evening ? '✅' : '⏳';

    morningBtn.disabled = status.morning || (day > 21);
    dayBtn.disabled = status.day || (day > 21);
    eveningBtn.disabled = status.evening || (day > 21);

    morningBtn.textContent = status.morning ? '✅ Получено' : '🌅 Получить утреннюю практику';
    dayBtn.textContent = status.day ? '✅ Принято' : '☀️ Получить вопрос';

    renderReports();

    if (state.reports.length >= 21 && !state.finished) {
        state.finished = true;
        generateAnalysis();
        saveState();
    }

    if (state.finished) {
        finalResult.style.display = 'block';
        renderAnalysis();
    } else {
        finalResult.style.display = 'none';
    }

    hideAllScreens();
    dashboardScreen.style.display = 'block';
    dashboardScreen.classList.add('active');
    pushHistory('dashboard');
}

function renderReports() {
    if (state.reports.length === 0) {
        reportsList.innerHTML = '<p class="empty-history">Пока нет отчётов. Заполни вечерний блок.</p>';
        return;
    }
    const sorted = [...state.reports].sort((a, b) => a.day - b.day);
    let html = '';
    sorted.forEach(r => {
        html += `
            <div class="report-item">
                <span class="day-tag">День ${r.day}</span>
                <div class="report-text">${escapeHtml(r.text)}</div>
                <button class="edit-report-btn" data-day="${r.day}" title="Редактировать">✏️</button>
            </div>
        `;
    });
    reportsList.innerHTML = html;
}

