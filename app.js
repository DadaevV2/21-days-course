let backBtn = null;
let navigationHistory = [];

function createBackButton() {
    if (backBtn) { backBtn.remove(); backBtn = null; }
    backBtn = document.createElement('button');
    backBtn.className = 'btn ghost small back-btn';
    backBtn.textContent = '← Назад';
    backBtn.style.cssText = 'position:fixed;top:20px;left:20px;z-index:9999;color:#8a8aa8;background:rgba(20,20,40,0.8);backdrop-filter:blur(10px);border:1px solid rgba(255,255,255,0.1);padding:10px 18px;border-radius:12px;font-size:14px;cursor:pointer;transition:all 0.3s;display:none;';
    backBtn.addEventListener('click', goBack);
    document.body.appendChild(backBtn);
    return backBtn;
}

function pushHistory(screen) { navigationHistory.push(screen); updateBackButton(); }

function goBack() {
    if (navigationHistory.length <= 1) return;
    navigationHistory.pop();
    const prevScreen = navigationHistory[navigationHistory.length - 1];
    switch(prevScreen) {
        case 'intro': showIntro(); break;
        case 'question': goToQuestion(); break;
        case 'register': showRegister(); break;
        case 'dashboard': renderDashboard(); break;
        default: showIntro();
    }
    updateBackButton();
}

function updateBackButton() { if (backBtn) backBtn.style.display = navigationHistory.length > 1 ? 'block' : 'none'; }

